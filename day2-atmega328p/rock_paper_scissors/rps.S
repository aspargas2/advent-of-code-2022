// Returns u32; upper u16 is part 1 solution, lower u16 is part 2 solution
.global solve
solve:
  //push r16

  // This can be done more simply but GAS is being a meanie
  ldi zl, pm_lo8(input_file)
  ldi zh, pm_hi8(input_file)
  lsl zh
  lsl zl
  adc zh, r1

  ldi r24, 0
  ldi r25, 0
  ldi r22, 0
  ldi r23, 0
main_loop:
  lpm r18, z+
  add r18, r1
  breq main_loop_break

  lpm r19, z+
  lpm r19, z+

  ldi r20, ('X' - 'A')
  sub r19, r20

  cp r18, r19
  breq draw
  call inc_r18_abc
  cp r19, r20
  breq win
lose:
  ldi r20, 0
  rjmp game_done_p1
draw:
  ldi r20, 3
  rjmp game_done_p1
win:
  ldi r20, 6
game_done_p1:
  add r24, r20
  adc r25, r1

  mov r20, r19
  ldi r21, ('A' - 1)
  sub r20, r21
  add r24, r20
  adc r25, r1

  ldi r20, 'B'
  cp r19, r20
  breq do_draw
  brlo do_lose
do_win:
  call inc_r18_abc
  mov r19, r20
  ldi r20, 6
  rjmp game_done_p2
do_draw:
  mov r19, r18
  ldi r20, 3
  rjmp game_done_p2
do_lose:
  call dec_r18_abc
  mov r19, r20
  ldi r20, 0
game_done_p2:
  add r22, 20
  adc r23, r1

  mov r20, r19
  ldi r21, ('A' - 1)
  sub r20, r21
  add r22, r20
  adc r23, r1

  lpm r18, z+
  rjmp main_loop

main_loop_break:

  //pop r16
  ret

// Result in r20, clobbers r21
inc_r18_abc:
  mov r20, r18
  inc r20
  ldi r21, 'D'
  cp r20, r21
  brne skip_sub
  ldi r20, 'A'
skip_sub:
  ret

// Result in r20, clobbers r21
dec_r18_abc:
  mov r20, r18
  dec r20
  ldi r21, ('A' - 1)
  cp r20, r21
  brne skip_add
  ldi r20, 'C'
skip_add:
  ret

